"""Unified retrieval benchmark: retriever x index.

Compares retriever strategies on our data and prints a single table with the metrics:
Recall@1, Recall@10, MRR@10 plus latency p50/p95/p99.

Metrics are computed SOURCE-LEVEL (gold = relevant_sources from retrieval_gold.json):
  Recall@1   — the top-1 source is relevant
  Recall@10  — a relevant source is in the top-10 sources
  MRR@10     — 1/rank of the first relevant source (within top-10)
  multiFC@10 — coverage on multi-doc questions (all relevant sources in top-10)
  latency    — time of the retrieval step alone (excludes query embedding)

Retrievers:
  dense        — cosine only (bi-encoder)
  hybrid_plus  — BM25Plus + dense, fused with RRF
  reranker     — dense top-N -> CrossEncoder rerank -> top-k (requires sentence-transformers)

Run:
  python eval/benchmark.py
  python eval/benchmark.py --collections docs_chunks --retrievers dense reranker
"""
from __future__ import annotations
import argparse
import csv
import json
import pathlib
import sys
import time

import numpy as np

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[1]))

from rank_bm25 import BM25Plus                                   # noqa: E402
from app.config import settings                                 # noqa: E402
from app.embeddings import embed_texts                          # noqa: E402
from evaluate import load_corpus, dense_search, hybrid_search, _tok  # noqa: E402

HERE = pathlib.Path(__file__).parent
POOL = 40   # chunk window width -> unique sources -> top-10 (also the candidate pool for the reranker)


def ordered_sources(chunk_ids: list[str], id_to_src: dict[str, str]) -> list[str]:
    seen, out = set(), []
    for cid in chunk_ids:
        s = id_to_src.get(cid)
        if s and s not in seen:
            seen.add(s)
            out.append(s)
    return out


def pct(xs: list[float], p: float) -> float:
    return float(np.percentile(xs, p)) if xs else 0.0


def run_retriever(name, qvs, qtexts, vecs, ids, texts, bm25):
    """Returns (ranked_chunkids_per_query, latency_ms_per_query)."""
    id_to_text = dict(zip(ids, texts))
    score_fn = None
    if name == "reranker":
        from app.rerank import score as score_fn   # imported here only, to avoid pulling in torch otherwise
    ranked_all, lat = [], []
    for qv, qt in zip(qvs, qtexts):
        t0 = time.perf_counter()
        if name == "dense":
            ranked = dense_search(qv, vecs, ids, top_k=POOL)
        elif name == "hybrid_plus":
            ranked = hybrid_search(qv, qt, bm25, vecs, ids, top_k=POOL)
        elif name == "reranker":
            pool = dense_search(qv, vecs, ids, top_k=POOL)
            scores = score_fn(qt, [id_to_text[c] for c in pool])
            ranked = [pool[i] for i in np.argsort(scores)[::-1]]
        else:
            raise ValueError(name)
        lat.append((time.perf_counter() - t0) * 1000)
        ranked_all.append(ranked)
    return ranked_all, lat


def compute_metrics(ranked_lists, dataset, id_to_src) -> dict:
    answerable = [ex for ex in dataset if ex["type"] in ("single", "multi")]
    r1 = r10 = mrr = 0.0
    multi_full = multi_n = 0
    for ex, ranked in zip(dataset, ranked_lists):
        if ex["type"] == "abstain":
            continue
        top10 = ordered_sources(ranked, id_to_src)[:10]
        rel = set(ex["relevant_sources"])
        r1 += 1 if (top10 and top10[0] in rel) else 0
        r10 += 1 if (rel & set(top10)) else 0
        rank = next((i + 1 for i, s in enumerate(top10) if s in rel), 0)
        mrr += 1.0 / rank if rank else 0.0
        if ex["type"] == "multi":
            multi_n += 1
            multi_full += 1 if rel <= set(top10) else 0
    n = max(len(answerable), 1)
    return {"recall@1": r1 / n, "recall@10": r10 / n, "mrr@10": mrr / n,
            "multiFC@10": multi_full / max(multi_n, 1), "n": len(answerable)}


def save_results(rows, dataset, outdir: str) -> None:
    """Persist measured results as durable artifacts (CSV + markdown), not just stdout."""
    out = pathlib.Path(outdir)
    out.mkdir(parents=True, exist_ok=True)
    n_ans = rows[0][2]["n"]
    cols = ["index", "retriever", "recall@1", "recall@10", "mrr@10", "multiFC@10",
            "p50_ms", "p95_ms", "p99_ms", "n_answerable"]
    with (out / "benchmark_results.csv").open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(cols)
        for coll, rname, m, lat in rows:
            w.writerow([coll, rname, f"{m['recall@1']:.3f}", f"{m['recall@10']:.3f}",
                        f"{m['mrr@10']:.3f}", f"{m['multiFC@10']:.3f}",
                        f"{pct(lat, 50):.1f}", f"{pct(lat, 95):.1f}", f"{pct(lat, 99):.1f}", m["n"]])
    md = ["# Retrieval benchmark — measured results", "",
          f"_Generated by `eval/benchmark.py` · gold `retrieval_gold.json` "
          f"({len(dataset)} questions, {n_ans} answerable) · source-level, top-10 sources._", "",
          "| index | retriever | R@1 | R@10 | MRR@10 | multiFC | p50 ms | p95 ms | p99 ms |",
          "|---|---|---|---|---|---|---|---|---|"]
    for coll, rname, m, lat in rows:
        md.append(f"| {coll} | {rname} | {m['recall@1']:.3f} | {m['recall@10']:.3f} | "
                  f"{m['mrr@10']:.3f} | {m['multiFC@10']:.3f} | "
                  f"{pct(lat, 50):.1f} | {pct(lat, 95):.1f} | {pct(lat, 99):.1f} |")
    (out / "benchmark_results.md").write_text("\n".join(md) + "\n", encoding="utf-8")
    print(f"\nSaved artifacts: {out / 'benchmark_results.csv'}  +  .md")


def main() -> None:
    ap = argparse.ArgumentParser(description="Retrieval benchmark.")
    ap.add_argument("--collections", nargs="+", default=["docs_chunks", "docs_chunks_nocode"])
    ap.add_argument("--retrievers", nargs="+", default=["dense", "hybrid_plus", "reranker"])
    ap.add_argument("--dataset", default=str(HERE / "retrieval_gold.json"))
    ap.add_argument("--out", default=str(HERE / "results"), help="where to save the CSV/markdown")
    args = ap.parse_args()

    dataset = json.loads(pathlib.Path(args.dataset).read_text(encoding="utf-8"))
    queries = [ex["query"] for ex in dataset]
    print(f"Dataset: {len(dataset)} questions | embedding queries (once, shared across retrievers) ...")
    qvs = [np.array(v, dtype=np.float32) for v in embed_texts(queries)]

    rows = []
    for coll in args.collections:
        ids, srcs, texts, vecs = load_corpus(coll)
        id_to_src = dict(zip(ids, srcs))
        bm25 = BM25Plus([_tok(t) for t in texts]) if "hybrid_plus" in args.retrievers else None
        print(f"  [{coll}] {len(ids)} chunks — running {args.retrievers} ...")
        for rname in args.retrievers:
            ranked, lat = run_retriever(rname, qvs, queries, vecs, ids, texts, bm25)
            rows.append((coll, rname, compute_metrics(ranked, dataset, id_to_src), lat))

    hdr = (f"{'index':<20}{'retriever':<13}{'R@1':>7}{'R@10':>8}{'MRR@10':>9}"
           f"{'multiFC':>9}{'p50ms':>10}{'p95ms':>10}{'p99ms':>10}")
    print("\n" + "=" * len(hdr))
    print(f"SOURCE-LEVEL BENCHMARK  (answerable={rows[0][2]['n']} questions, top-10 sources)")
    print("=" * len(hdr))
    print(hdr)
    print("-" * len(hdr))
    for coll, rname, m, lat in rows:
        print(f"{coll:<20}{rname:<13}{m['recall@1']:>7.3f}{m['recall@10']:>8.3f}{m['mrr@10']:>9.3f}"
              f"{m['multiFC@10']:>9.3f}{pct(lat, 50):>10.1f}{pct(lat, 95):>10.1f}{pct(lat, 99):>10.1f}")
    print("\nlatency = time of the retrieval step (excludes query embedding). reranker: CrossEncoder on "
          f"{POOL} pairs/query.")
    save_results(rows, dataset, args.out)


if __name__ == "__main__":
    main()